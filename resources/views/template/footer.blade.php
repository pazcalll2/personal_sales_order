    function addToChart(product, qty, currentPage, id) {
        console.log('push')
        exists = false
        // console.log(product)
        if (carts != null && carts.length != 0) {
            carts.forEach((cart, index) => {
                if (cart.id == product.id) {
                    exists = true
                    cart.qty = parseInt(cart.qty) + parseInt(qty)
